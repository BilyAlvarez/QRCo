  _lang: 'ES',

  setLang(lang) {
    this._lang = lang
    document.documentElement.lang = lang.toLowerCase()
  },

  get(key) {
    return this[this._lang][key] || key
  },

  translateAll() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.textContent = this.get(el.dataset.i18n)
    })
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = this.get(el.dataset.i18nPlaceholder)
    })
    document.querySelectorAll('[data-i18n-op]').forEach(el => {
      el.textContent = this.get(el.dataset.i18nOp)
    })
  },

  toggle() {
    const newLang = this._lang === 'ES' ? 'EN' : 'ES'
    this.setLang(newLang)
    this.translateAll()
    document.getElementById('langLabel').textContent = this.get('toolbar.lang')
    const user = Storage.getUser()
    if (user) {
      user.lang = newLang
      Storage.setUser(user)
    }
  }
